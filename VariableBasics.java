package BASICS_OF_JAVA;

// Variables are containers that store value.

public class VariableBasics {
    public static void main(String[] args){
        int x = 5; // Here x is treated as a container which stores value 5 till now.
        // Initialization and Declaration of variable.
        int y ; // Declaration
        y = 23; // Initialization
        System.out.println(x);
        System.out.println(y);
        // Modifying values of variables
        y = 87;
        System.out.print(y);
    }
}
