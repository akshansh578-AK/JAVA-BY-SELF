package BASICS_OF_JAVA;

public class DivisonOfDataTypes {
    public static void main(String[] args){
        double X = 3/4;     // int/int = int
        double Y = 4.0/5;   // double/int = double
        double Z = 9/8.0;   // int/double = double
        double W = 6.0/4.0;  // double/double = double
        System.out.println(X);
        System.out.println(Y);
        System.out.println(Z);
        System.out.print(W);



    }
}
